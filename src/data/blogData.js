export const getBlogBySlug = (slug) => {
  return blogData.find(blog => blog.slug === slug);
};

export const getBlogsByCategory = (category) => {
  if (category === 'All') return blogData;
  return blogData.filter(blog => blog.category === category);
};

export const getRelatedBlogs = (currentBlogId, category, limit = 3) => {
  return blogData
    .filter(blog => blog.id !== currentBlogId && blog.category === category)
    .slice(0, limit);
};
